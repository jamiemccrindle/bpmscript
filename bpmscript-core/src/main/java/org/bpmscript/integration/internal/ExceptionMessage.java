package org.bpmscript.integration.internal;

/**
 * An exception message
 */
public class ExceptionMessage extends AbstractMessage implements IExceptionMessage {
    
    private static final long serialVersionUID = 4432205367877656708L;

    private Throwable throwable = null;

    public ExceptionMessage(String correlationId, Throwable throwable) {
        super(correlationId);
        this.throwable = throwable;
    }

    public Throwable getThrowable() {
        return throwable;
    }

    public void setThrowable(Throwable throwable) {
        this.throwable = throwable;
    }
}
